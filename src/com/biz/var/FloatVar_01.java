package com.biz.var;

public class FloatVar_01 {

	public static void main(String[] args) {
		
		
		//실수형
		float fNum1 = 30f;
		System.out.println(fNum1);
		
		float fNum2 = 50.0f;
		
		System.out.println(fNum2 / fNum1);
		
		fNum2 = 5f;
		System.out.println(fNum1 / fNum2);
		
		// float는 값 뒤에 f라는 문자를 넣어주어야 한다
		
		

	}

}
